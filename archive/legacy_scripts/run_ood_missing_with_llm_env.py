"""
Run BERT_ML OOD missing/invalid target outputs with the fewshot `llm` conda env.

Default behavior is safe:
  - Relaunches itself with C:\\Users\\HK\\miniconda3\\envs\\llm\\python.exe.
  - Detects Traditional-ML sparse/LOCO OOD target outputs.
  - Excludes HEA YS and Steel YS by default, matching the current comparison scope.
  - Dry-runs unless --execute is passed.

Typical commands from the BERT_ML repo root:

  # 1) Inspect what would be run.
  python Scripts\\run_ood_missing_with_llm_env.py --mode missing-only

  # 2) Actually backup and rerun only missing/incomplete target outputs.
  python Scripts\\run_ood_missing_with_llm_env.py --execute --mode missing-only --jobs 1

  # 3) Rerun every inconsistent BERT_ML target listed in comparison_report_v2.csv.
  python Scripts\\run_ood_missing_with_llm_env.py --execute --mode report-inconsistent --include-ys-tasks --jobs 1

  # 4) Also rerun existing sparse outputs whose split was generated by a different env.
  python Scripts\\run_ood_missing_with_llm_env.py --execute --mode all-issues --jobs 1

The script writes a manifest under:
  output\\ood_repair_backups\\llm_env_repair_YYYYMMDD_HHMMSS\\repair_manifest.json
"""

