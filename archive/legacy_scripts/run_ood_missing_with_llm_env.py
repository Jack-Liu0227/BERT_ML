def relaunch_with_llm_if_needed(args: argparse.Namespace) -> int | None:
    if args.no_relaunch:
        return None
    llm_python = args.llm_python
    if not llm_python.exists():
        raise FileNotFoundError(f"llm Python not found: {llm_python}")
    if _same_executable(Path(sys.executable), llm_python):
        return None

    cmd = [str(llm_python), str(Path(__file__).resolve()), *sys.argv[1:], "--no-relaunch"]
    print("[INFO] Relaunching with llm env:", flush=True)
    print("       " + " ".join(f'"{part}"' if " " in part else part for part in cmd), flush=True)
    result = subprocess.run(cmd, cwd=str(REPO_ROOT), text=True)
    return int(result.returncode)


def is_missing_reason(reason: str) -> bool:
    lowered = reason.lower()
    return (
        lowered.startswith("missing_")
        or ":missing_" in lowered
        or "missing_result_dir" in lowered
        or "missing_model_manifest" in lowered
        or "missing_model_metrics" in lowered
        or "missing_loco_manifest" in lowered
        or "missing_loco_folds" in lowered
        or "missing_trace" in lowered
        or "unreadable_trace" in lowered
    )


def is_split_reason(reason: str) -> bool:
    lowered = reason.lower()
    return (
        "id_mismatch" in lowered
        or "fold_count_mismatch" in lowered
        or "fold_index_mismatch" in lowered
        or "split_validation_error" in lowered
    )


def issue_matches_mode(issue, mode: str) -> bool:
    reasons = [str(reason) for reason in issue.reasons]
    if mode == "missing-only":
        return any(is_missing_reason(reason) for reason in reasons)
    if mode == "split-only":
        return any(is_split_reason(reason) for reason in reasons)
    return True


def load_report_task_filter(args: argparse.Namespace) -> set[tuple[str, str, str]]:
    """Return (method, alloy, target) tuples from the comparison report."""
    import pandas as pd  # Imported lazily after env relaunch.

    report_path = args.comparison_report
    if not report_path.is_absolute():
        report_path = REPO_ROOT / report_path
    if not report_path.exists():
        raise FileNotFoundError(f"comparison report not found: {report_path}")

    frame = pd.read_csv(report_path)
    required = {"method", "case", "status"}
    missing = required - set(frame.columns)
    if missing:
        raise ValueError(f"comparison report missing columns: {sorted(missing)}")

    mask = frame["status"].astype(str).ne("ok")
    if args.include_value_diffs and "value_equal" in frame.columns:
        mask = mask | frame["value_equal"].astype(str).str.lower().eq("false")

    selected = set()
    for _, row in frame.loc[mask].iterrows():
        method = str(row["method"])
        case = str(row["case"])
        if method not in set(args.methods):
            continue
        if case not in CASE_TO_TARGET:
            print(f"[WARN] unknown case in comparison report; skipped: {case}")
            continue
        alloy, target = CASE_TO_TARGET[case]
        if not args.include_ys_tasks and (alloy, target) in DEFAULT_EXCLUDED_TARGETS:
            continue
        selected.add((method, alloy, target))
    return selected


def filter_issues(issues: Iterable, args: argparse.Namespace, report_tasks: set[tuple[str, str, str]] | None = None) -> list:
    selected = []
    method_set = set(args.methods)
    for issue in issues:
        if issue.method not in method_set:
            continue
        if not args.include_ys_tasks and (issue.alloy, issue.target) in DEFAULT_EXCLUDED_TARGETS:
            continue
        if report_tasks is not None:
            if (issue.method, issue.alloy, issue.target) not in report_tasks:
                continue
        elif not issue_matches_mode(issue, args.mode):
            continue
        selected.append(issue)
    return selected


def method_sort_index(method: str) -> int:
    try:
        return OOD_METHOD_ORDER.index(method)
    except ValueError:
        return len(OOD_METHOD_ORDER)


def clear_progress_for_selected(repair, issues: list) -> None:
    """Remove progress entries so a rerun cannot be skipped as already successful."""
    progress_file = REPO_ROOT / ".batch_progress_ood.json"
    for method in sorted({issue.method for issue in issues}, key=method_sort_index):
        config_name = ML_CONFIGS_ALL[method]
        keys = [
            repair.task_key(issue.alloy, issue.target, model)
            for issue in issues
            if issue.method == method
            for model in ML_MODELS
        ]
        repair.clear_progress_entries(progress_file, [config_name], keys)


